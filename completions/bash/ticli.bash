                ;;
            ticli,set)
                cmd="ticli__set"
                ;;
            ticli,source)
                cmd="ticli__source"
                ;;
            ticli,strlen)
                cmd="ticli__strlen"
                ;;
            ticli__help,count)
                cmd="ticli__help__count"
                ;;
            ticli__help,delete)
                cmd="ticli__help__delete"
                ;;
            ticli__help,get)
                cmd="ticli__help__get"
                ;;
