_ticli() {
    local i cur prev opts cmds
    COMPREPLY=()
    cur="${COMP_WORDS[COMP_CWORD]}"
    prev="${COMP_WORDS[COMP_CWORD-1]}"
    cmd=""
    opts=""

    for i in ${COMP_WORDS[@]}
    do
        case "${cmd},${i}" in
            ",$1")
                cmd="ticli"
                ;;
            ticli,.)
                cmd="ticli__source"
                ;;
            ticli,cnt)
                cmd="ticli__count"
                ;;
            ticli,count)
